#include <iostream>         // для std::cout, std::cerr
#include <unordered_map>    // для std::unordered_map (хэш-таблица)
#include <vector>           // для std::vector

class Solution
{
public:
    // Возвращает индексы двух чисел в массиве nums, сумма которых равна target.
    // Если пара найдена, возвращается вектор из двух индексов {index1, index2}.
    // Иначе возвращается пустой вектор.
    std::vector<int> twoSum(const std::vector<int>& nums, int target)
    {
        // Хэш-таблица: значение -> индекс. Позволяет за O(1) в среднем
        // проверить, встречалось ли ранее число, необходимое для суммы.
        std::unordered_map<int, int> mp;

        // Проходим по всем элементам массива.
        // Приведение (int)nums.size() используется, чтобы сравнивать одинаковые типы.
        // (В идеале можно использовать size_t для i, но код использует int.)
        for (int i = 0; i < (int)nums.size(); ++i)
        {
            // Число, которое нам нужно найти среди предыдущих элементов,
            // чтобы вместе с nums[i] получить target.
            int need = target - nums[i];

            // Ищем "need" в хэш-таблице.
            auto it = mp.find(need);

            // Если найдено, значит ранее встретилось такое число и его индекс хранится в mp.
            // Возвращаем пару индексов: индекс ранее встретившегося числа и текущий индекс.
            if (it != mp.end()) return { it->second, i };

            // Если не найдено, сохраняем текущий элемент в таблицу:
            // ключ = значение числа, значение = индекс.
            // Если число повторяется, последнее присваивание перезапишет индекс;
            // это не повредит, так как при наличии пары мы обязательно найдём
            // предыдущий индекс требуемого числа раньше, чем перезапишем нужный.
            mp[nums[i]] = i;
        }

        // Если не нашлось никаких двух чисел с нужной суммой — вернуть пустой вектор.
        return {};
    }
};

int main()
{
    Solution sol;

    // Пример теста: ожидаем, что числа 2 и 7 (индексы 0 и 1) дают сумму 9.
    std::vector<int> nums = { 2,7,11,15 };

    // Вызываем функцию решения
    auto res = sol.twoSum(nums, 9);

    // Проверяем корректность результата:
    // Учитываем оба порядка индексов (0,1) или (1,0).
    if (res.size() == 2 && ((res[0] == 0 && res[1] == 1) || (res[0] == 1 && res[1] == 0)))
    {
        // Успех: печатаем OK и возвращаем 0 — полезно для CI (ctest/автотесты).
        std::cout << "OK\n";
        return 0;
    }
    else
    {
        // Ошибка: печатаем в stderr и возвращаем ненулевой код — CI пометит тест как упавший.
        std::cerr << "Test failed\n";
        return 1;
    }
}


